package com.forms;

public class UserForm {
    private int id=0;
    private String userId=null;
    private String userName=null;
    private String userPwd=null;
    private String userPhNum=null;
    private String userHeadImg=null;
    private String userEmail=null;
    private String userAddress=null;
    private int userAlbumNum=0;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public String getUserPwd() {
        return userPwd;
    }

    public void setUserPwd(String userPwd) {
        this.userPwd = userPwd;
    }

    public String getUserPhNum() {
        return userPhNum;
    }

    public void setUserPhNum(String userPhNum) {
        this.userPhNum = userPhNum;
    }

    public String getUserHeadImg() {
        return userHeadImg;
    }

    public void setUserHeadImg(String userHeadImg) {
        this.userHeadImg = userHeadImg;
    }

    public String getUserEmail() {
        return userEmail;
    }

    public void setUserEmail(String userEmail) {
        this.userEmail = userEmail;
    }

    public String getUserAddress() {
        return userAddress;
    }

    public void setUserAddress(String userAddress) {
        this.userAddress = userAddress;
    }

    public int getUserAlbumNum() {
        return userAlbumNum;
    }

    public void setUserAlbumNum(int userAlbumNum) {
        this.userAlbumNum = userAlbumNum;
    }
}
